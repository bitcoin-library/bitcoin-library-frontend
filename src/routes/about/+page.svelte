	<h1 class="mb-3 font-bold">Why a Bitcoin Library</h1>
	<p>
		You asked almost right question. It's more "Why a Bitcoin Library on
		Nostr?" Nostr is the only technology where a Library about Bitcoin related
		content makes sense. Education has to be censorship resistant as our money
		should be. This is why this library is built on Nostr.
	</p>

	<h1 class="my-3 font-bold">How does it work?</h1>

	Everybody can participate and fill the lib with content. Easiest it is using
	the<a
		href="https://addons.mozilla.org/en-US/firefox/addon/bitcoin-library-extension/"
		>Bitcoin Library Browser Extension</a
	>
	for Firefox. Just got across a good educational resource about Bitcoin? Open the
	exstension and add it to the library!

	<h1 class="my-3 font-bold">Create Lists of Resources</h1>

	Build your own topic based lists: log in and click the "+" symbol. Don't
	forget to share them with your friends! (To be implemented soon)
